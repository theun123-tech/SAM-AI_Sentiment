"""
policy_engine.py — Rule-based policy engine for dialogue management

Given:
    - NLUResult (what the user's utterance MEANS)
    - Current MeetingState (where we are in the meeting)

Decides ONE of these actions for Sam to take:

    - respond_direct       Sam gives a regular answer (goes to Agent.py)
    - respond_with_research  Needs Jira/web search (same pipeline as today)
    - ask_clarification    Utterance is ambiguous — Sam asks for specifics
    - transition_topic     Mark current topic done, move to next agenda item
    - scope_redirect       User brought up out-of-scope — Sam politely redirects
    - stay_silent          Nothing to say (e.g., user addressed another human)
    - deliver_recap        End of meeting — Sam speaks the recap

Design:
    - Pure rules. No LLM calls (keeps latency low).
    - Deterministic. Same inputs → same output. Easier to debug.
    - Conservative. When in doubt, respond_direct (the "safe" action).
    - Logs the reasoning for every decision (for later tuning).

Rule priority (first match wins, top to bottom):
    1. meeting_end_signal intent    → deliver_recap
    2. addressed_to_other intent    → stay_silent
    3. scope_violation intent       → scope_redirect
    4. hypothetical intent          → respond_direct (Sam can engage naturally)
    5. acknowledgment intent        → stay_silent (let humans chat)
    6. topic_close + topic_signal   → transition_topic
    7. clarification_request        → respond_direct (Sam clarifies)
    8. question with ambiguous refs → ask_clarification
    9. question with ticket entities → respond_direct (can use cache/Jira)
    10. commitment/statement        → respond_direct
    11. default                     → respond_direct
"""

import time
from dataclasses import dataclass, field
from enum import Enum
from typing import Optional



# ══════════════════════════════════════════════════════════════════════════
# Action types
# ══════════════════════════════════════════════════════════════════════════

class PolicyAction(str, Enum):
    """All possible actions Sam can take after a user turn."""
    RESPOND_DIRECT = "respond_direct"
    RESPOND_WITH_RESEARCH = "respond_with_research"
    ASK_CLARIFICATION = "ask_clarification"
    TRANSITION_TOPIC = "transition_topic"
    SCOPE_REDIRECT = "scope_redirect"
    STAY_SILENT = "stay_silent"
    DELIVER_RECAP = "deliver_recap"


@dataclass
class PolicyDecision:
    """What Sam should do next + context for how to do it."""
    action: PolicyAction
    reasoning: str                    # human-readable rationale (for logs)

    # Optional context per action type:
    clarification_options: list[str] = field(default_factory=list)
    """For ASK_CLARIFICATION — the options Sam should enumerate."""

    scope_topic_raised: str = ""
    """For SCOPE_REDIRECT — the out-of-scope topic user mentioned."""

    transition_from: Optional[str] = None
    transition_to: Optional[str] = None
    """For TRANSITION_TOPIC — source and target agenda item titles."""

    confidence: float = 1.0
    """How confident the policy is in this decision (0-1)."""

    # Extras that downstream nodes may use
    use_ticket_cache: bool = False
    """True if ticket data in state should be used instead of live Jira."""

    def to_dict(self) -> dict:
        return {
            "action": self.action.value,
            "reasoning": self.reasoning,
            "clarification_options": list(self.clarification_options),
            "scope_topic_raised": self.scope_topic_raised,
            "transition_from": self.transition_from,
            "transition_to": self.transition_to,
            "confidence": self.confidence,
            "use_ticket_cache": self.use_ticket_cache,
        }


# ══════════════════════════════════════════════════════════════════════════
# Confidence threshold — below this, we ignore LLM extractions
# ══════════════════════════════════════════════════════════════════════════

# Entities below this confidence are considered noise
ENTITY_CONFIDENCE_THRESHOLD = 0.7

# Commitments below this confidence don't trigger extraction
COMMITMENT_CONFIDENCE_THRESHOLD = 0.7

# Meeting is "near the end" if within this many minutes of planned duration
MEETING_END_WINDOW_MINUTES = 2


# ══════════════════════════════════════════════════════════════════════════
# PolicyEngine
# ══════════════════════════════════════════════════════════════════════════

class PolicyEngine:
    """Decides what Sam should do given NLU output + current state.

    Stateless — all decisions are pure functions of (nlu, state).
    Safe to call from any thread / coroutine.

    Usage:
        engine = PolicyEngine(tag="[S:abc]")
        decision = engine.decide(nlu_result, meeting_state)

        if decision.action == PolicyAction.RESPOND_DIRECT:
            # call Agent.handle()
        elif decision.action == PolicyAction.STAY_SILENT:
            # do nothing
        elif decision.action == PolicyAction.DELIVER_RECAP:
            # generate + speak recap
        # ...
    """

    def __init__(self, tag: str = ""):
        self.tag = tag
        # Track decision counts for later observability
        self._decision_counts: dict[str, int] = {}

    def decide(
        self,
        nlu: NLUResult,
        state: MeetingState,
    ) -> PolicyDecision:
        """Main entry point — returns a PolicyDecision based on rules.

        Never raises. Always returns a valid decision, defaulting to
        RESPOND_DIRECT if no rule matches.
        """
        decision = self._apply_rules(nlu, state)
        self._record(decision.action)
        print(f"[Policy] {self.tag} → {decision.action.value} "
              f"(conf={decision.confidence:.2f}) — {decision.reasoning[:80]}")
        return decision

    def get_stats(self) -> dict:
        """Return how many times each action was chosen (for monitoring)."""
        return dict(self._decision_counts)

    # ══════════════════════════════════════════════════════════════════════
    # Rule chain (priority order, first match wins)
    # ══════════════════════════════════════════════════════════════════════

    def _apply_rules(self, nlu: NLUResult, state: MeetingState) -> PolicyDecision:
        intent = nlu.intent.lower()

        # ── Rule 1: meeting_end_signal → deliver recap (gated) ─────────
        # Phase 6 step 2: three preconditions to avoid spurious recaps.
        if intent == "meeting_end_signal":
            # Gate 1: already delivered once this session?
            if state.get("recap_delivered", False):
                return PolicyDecision(
                    action=PolicyAction.STAY_SILENT,
                    reasoning="Recap already delivered earlier",
                    confidence=0.9,
                )
            # Gate 2: meeting young enough that "bye" is probably spurious
            import time as _time
            started = state.get("meeting_started_at", 0.0) or 0.0
            if started > 0:
                duration = _time.time() - started
                if duration < 120:
                    return PolicyDecision(
                        action=PolicyAction.STAY_SILENT,
                        reasoning=f"Meeting too young ({duration:.0f}s) — likely spurious farewell",
                        confidence=0.85,
                    )
            # Gate 3: nothing captured worth recapping
            has_content = (
                len(state.get("commitments_open", []) or []) > 0
                or len(state.get("topics_resolved", []) or []) > 0
                or len(state.get("open_questions", []) or []) > 0
            )
            if not has_content:
                return PolicyDecision(
                    action=PolicyAction.STAY_SILENT,
                    reasoning="Nothing captured — short session, skipping recap",
                    confidence=0.9,
                )
            # All gates pass — deliver recap
            return PolicyDecision(
                action=PolicyAction.DELIVER_RECAP,
                reasoning="End-of-meeting signal + content present",
                confidence=0.95,
            )

        # ── Rule 2: addressed_to_other → stay silent ────────────────────
        # User is talking to another human, not Sam.
        if intent == "addressed_to_other":
            named_people = nlu.get_named_people()
            return PolicyDecision(
                action=PolicyAction.STAY_SILENT,
                reasoning=(f"Speaker addressed {named_people[0]}, not Sam"
                           if named_people else "Speaker addressed another person, not Sam"),
                confidence=0.9,
            )

        # ── Rule 3: scope_violation → redirect ──────────────────────────
        if intent == "scope_violation" or nlu.scope_signal == "out_of_scope":
            # Find the out-of-scope topic raised
            topic_entities = [
                e for e in nlu.entities
                if e.get("type") == "topic"
                and e.get("confidence", 0) >= ENTITY_CONFIDENCE_THRESHOLD
            ]
            topic_raised = topic_entities[0]["value"] if topic_entities else ""
            return PolicyDecision(
                action=PolicyAction.SCOPE_REDIRECT,
                reasoning=f"User raised out-of-scope topic: {topic_raised or 'unspecified'}",
                scope_topic_raised=topic_raised,
                confidence=0.85,
            )

        # ── Rule 4: acknowledgment → stay silent ────────────────────────
        # "Yeah", "okay", "thanks" — no meaningful response needed.
        # But if Sam was waiting on a question, maybe keep engaged.
        if intent == "acknowledgment":
            # Check if Sam's last turn ended with a question — then we DO want to engage
            sam_last = state.get("sam_last_response", "").strip()
            if sam_last.endswith("?"):
                return PolicyDecision(
                    action=PolicyAction.RESPOND_DIRECT,
                    reasoning="Acknowledgment to Sam's question — engage",
                    confidence=0.6,
                )
            return PolicyDecision(
                action=PolicyAction.STAY_SILENT,
                reasoning="Simple acknowledgment — no response needed",
                confidence=0.8,
            )

        # ── Rule 5: topic_close with moving_on signal → transition ─────
        if intent == "topic_close" and nlu.topic_signal == "moving_on":
            return self._build_transition_decision(nlu, state)

        # Also: explicit "moving_on" topic_signal triggers transition even
        # without topic_close intent (e.g., "let's do deployment next")
        if nlu.topic_signal == "moving_on":
            # Only if there's actually a next topic
            current = get_current_topic(state)
            agenda = state.get("agenda", [])
            current_idx = state.get("current_topic_index", -1)
            if current_idx < len(agenda) - 1:
                return self._build_transition_decision(nlu, state)

        # ── Rule 6: clarification_request → respond directly ───────────
        if intent == "clarification_request":
            return PolicyDecision(
                action=PolicyAction.RESPOND_DIRECT,
                reasoning="User asked Sam to clarify — respond",
                confidence=0.9,
            )

        # ── Rule 7: ambiguous question → ask for specifics ─────────────
        if intent == "question" and self._is_question_ambiguous(nlu, state):
            options = self._suggest_clarification_options(nlu, state)
            return PolicyDecision(
                action=PolicyAction.ASK_CLARIFICATION,
                reasoning=f"Question is ambiguous — enumerate options: {options[:3]}",
                clarification_options=options,
                confidence=0.7,
            )

        # ── Rule 8: question with ticket entities → use cache if possible ─
        if intent == "question":
            ticket_keys = nlu.get_ticket_keys()
            if ticket_keys:
                # Are all referenced tickets pre-loaded? If yes, skip research.
                cached = state.get("pre_loaded_tickets", {})
                all_cached = all(tk in cached for tk in ticket_keys)

                # Freshness override: user wants FRESH data, not cache
                if nlu.freshness_hint:
                    return PolicyDecision(
                        action=PolicyAction.RESPOND_WITH_RESEARCH,
                        reasoning=(f"Ticket query with freshness hint "
                                   f"({ticket_keys}) — live fetch"),
                        confidence=0.85,
                    )

                if all_cached:
                    return PolicyDecision(
                        action=PolicyAction.RESPOND_DIRECT,
                        reasoning=f"Ticket(s) {ticket_keys} all cached — use cache",
                        use_ticket_cache=True,
                        confidence=0.9,
                    )
                else:
                    missing = [tk for tk in ticket_keys if tk not in cached]
                    return PolicyDecision(
                        action=PolicyAction.RESPOND_WITH_RESEARCH,
                        reasoning=f"Ticket(s) {missing} not cached — live fetch needed",
                        confidence=0.85,
                    )

            # Question without ticket entities — may need research
            # Heuristic: if utterance is short and general, respond directly;
            # if long or contains research keywords, allow research path.
            if self._needs_research(nlu):
                return PolicyDecision(
                    action=PolicyAction.RESPOND_WITH_RESEARCH,
                    reasoning="General question — allow research path",
                    confidence=0.7,
                )
            return PolicyDecision(
                action=PolicyAction.RESPOND_DIRECT,
                reasoning="Simple question — respond directly",
                confidence=0.75,
            )

        # ── Rule 9: commitment by speaker → ack + record (respond) ─────
        if intent in ("commitment", "commitment_by_other"):
            # Respond to acknowledge + capture. State update node handles
            # the actual commitment recording.
            return PolicyDecision(
                action=PolicyAction.RESPOND_DIRECT,
                reasoning=f"Commitment detected ({intent}) — ack and note",
                confidence=0.85,
            )

        # ── Rule 10: statement / topic_start → respond directly ────────
        if intent in ("statement", "topic_start"):
            return PolicyDecision(
                action=PolicyAction.RESPOND_DIRECT,
                reasoning=f"Intent={intent} — respond naturally",
                confidence=0.75,
            )

        # ── Rule 11: hypothetical → respond (Sam engages with speculation) ─
        if intent == "hypothetical":
            return PolicyDecision(
                action=PolicyAction.RESPOND_DIRECT,
                reasoning="Hypothetical discussion — engage",
                confidence=0.7,
            )

        # ── Default: respond directly (safe default) ───────────────────
        return PolicyDecision(
            action=PolicyAction.RESPOND_DIRECT,
            reasoning=f"No specific rule matched (intent={intent}) — default respond",
            confidence=0.5,
        )

    # ══════════════════════════════════════════════════════════════════════
    # Helper methods
    # ══════════════════════════════════════════════════════════════════════

    def _is_question_ambiguous(
        self,
        nlu: NLUResult,
        state: MeetingState,
    ) -> bool:
        """Heuristic for ambiguous questions.

        Current rules:
        - Very short question (<= 3 words) with no specific entities
        - Single vague word like "priorities" when multiple things could match
        - Pronoun reference ("it", "that one", "the ticket") with no context
        """
        # Get the user text from the most recent turn
        turns = state.get("conversation_turns", [])
        if not turns:
            return False
        last_user_turn = None
        for t in reversed(turns):
            if not t.is_sam:
                last_user_turn = t
                break
        if not last_user_turn:
            return False
        text = last_user_turn.text.lower().strip()
        word_count = len(text.split())

        # Heuristic 1: Very short, no entities → likely ambiguous
        significant_entities = [
            e for e in nlu.entities
            if e.get("confidence", 0) >= ENTITY_CONFIDENCE_THRESHOLD
        ]
        if word_count <= 3 and not significant_entities:
            # Check if it's a purely pronoun/vague reference
            vague_markers = {"it", "that", "this", "those", "these", "one", "ones", "them"}
            words = set(text.replace("?", "").replace(",", "").split())
            if words & vague_markers:
                return True

        # Heuristic 2: Ambiguous single-word topics
        ambiguous_words = {
            "priorities": ["ticket priority", "Q3 priorities", "general priorities"],
            "status": ["project status", "ticket status", "deployment status"],
            "timeline": ["project timeline", "deployment timeline", "Q3 timeline"],
        }
        for word in ambiguous_words:
            if word in text and word_count <= 4:
                # Short question centered on a vague term → ambiguous
                return True

        return False

    def _suggest_clarification_options(
        self,
        nlu: NLUResult,
        state: MeetingState,
    ) -> list[str]:
        """Generate candidate clarification options from state.

        Returns up to 3 specific alternatives Sam can enumerate for the user.
        """
        options: list[str] = []

        # Try agenda-based options (current or adjacent topics)
        agenda = state.get("agenda", [])
        current_idx = state.get("current_topic_index", -1)
        if agenda:
            if 0 <= current_idx < len(agenda):
                options.append(agenda[current_idx].title)
            # Add up to 2 more from adjacent items
            for i in [current_idx + 1, current_idx - 1]:
                if 0 <= i < len(agenda) and len(options) < 3:
                    options.append(agenda[i].title)

        # Dedupe and cap
        seen = set()
        deduped = []
        for o in options:
            if o not in seen:
                seen.add(o)
                deduped.append(o)
        return deduped[:3]

    def _needs_research(self, nlu: NLUResult) -> bool:
        """Heuristic: does this question likely need research (Jira/web)?"""
        # If any topic entities are present, probably needs context
        topic_entities = [
            e for e in nlu.entities
            if e.get("type") == "topic"
            and e.get("confidence", 0) >= 0.6
        ]
        if topic_entities:
            return True

        # Default: respond directly unless we have a strong research signal
        return False

    def _build_transition_decision(
        self,
        nlu: NLUResult,
        state: MeetingState,
    ) -> PolicyDecision:
        """Construct a TRANSITION_TOPIC decision with source/target context."""
        agenda = state.get("agenda", [])
        current_idx = state.get("current_topic_index", -1)

        current = None
        next_topic = None
        if 0 <= current_idx < len(agenda):
            current = agenda[current_idx].title
        if current_idx + 1 < len(agenda):
            next_topic = agenda[current_idx + 1].title

        return PolicyDecision(
            action=PolicyAction.TRANSITION_TOPIC,
            reasoning=(f"User signaled moving on (from {current or '?'} "
                       f"→ {next_topic or 'end'})"),
            transition_from=current,
            transition_to=next_topic,
            confidence=0.8,
        )

    def _record(self, action: PolicyAction) -> None:
        key = action.value
        self._decision_counts[key] = self._decision_counts.get(key, 0) + 1

