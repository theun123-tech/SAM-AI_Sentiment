# Utility: conversation_id hashing
# ══════════════════════════════════════════════════════════════════════════

def compute_conversation_id(participant_names: list[str]) -> str:
    """Stable conversation ID from sorted participant names.

    Same group of people meeting again → same ID → loads prior state.
    Different group → different ID → fresh start.

    Sam is excluded from the hash (bot isn't a human participant).
    Case-insensitive + whitespace-normalized so "Sahil" == "sahil " == " Sahil".
    """
    normalized = sorted(
        p.strip().lower()
        for p in participant_names
        if p and p.strip().lower() != "sam"
    )
    if not normalized:
        # Edge case: no participants yet — return a placeholder
        return "empty"
    key = "|".join(normalized)
    return hashlib.sha256(key.encode("utf-8")).hexdigest()[:16]


