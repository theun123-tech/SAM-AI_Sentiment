# MeetingState — the main schema (25 fields, 8 categories)
# ══════════════════════════════════════════════════════════════════════════

class MeetingState(TypedDict, total=False):
    """Comprehensive state object flowing through LangGraph.

    All fields are optional at construction (total=False) so we can incrementally
    populate. Use create_initial_state() to build a properly initialized instance.

    Serialization: all fields are JSON-safe. Dataclass sub-objects get converted
    via their .to_dict() methods in state_to_json().
    """

    # ── Agenda tracking (4 fields) ─────────────────────────────────────────
    agenda: Annotated[list[AgendaItem], replace_value]
    """Ordered list of planned topics. Set at session start, updated as topics progress."""

    current_topic_index: Annotated[int, replace_value]
    """Which agenda item is currently being discussed. -1 = no topic active."""

    topics_resolved: Annotated[list[str], append_list]
    """Agenda item IDs that have been marked complete."""

    topics_deferred: Annotated[list[str], append_list]
    """Agenda item IDs pushed to a future meeting."""

    # ── Scope tracking (3 fields) ──────────────────────────────────────────
    scope_in: Annotated[list[str], replace_value]
    """Topics/areas explicitly IN scope for this meeting."""

    scope_out: Annotated[list[str], replace_value]
    """Topics/areas explicitly OUT of scope. Sam redirects these."""

    scope_deviations: Annotated[list[ScopeDeviation], append_list]
    """Log of times user brought up out-of-scope content and how Sam handled it."""

    # ── Client profile (1 field) ───────────────────────────────────────────
    client_profile: Annotated[str, replace_value]
    """Free-text client/company profile from the 'Know About Them' UI feature.
    Used by all synthesis prompts to ground company-fact answers (e.g. who is
    the CEO, what do we do). Empty string when not configured. Read by:
    - websocket_server._build_client_profile_block_for_prompt()
    - Agent._get_client_profile_block()
    - Agent._format_meeting_state()
    Set once at DialogueManager.initialize() and unchanged for the session."""

    # ── Commitments (3 fields) ─────────────────────────────────────────────
    commitments_open: Annotated[list[Commitment], replace_value]
    """Action items pending. Updated on every turn — use replace to allow removal."""

    commitments_resolved: Annotated[list[Commitment], append_list]
    """Commitments completed during this call (moved from open)."""

    commitments_inherited: Annotated[list[Commitment], replace_value]
    """Open commitments carried forward from prior meetings."""

    # Phase 6 step 2: meeting-end recap flag
    recap_delivered: Annotated[bool, replace_value]
    """True once the end-of-meeting recap has been spoken. Prevents double-delivery."""

    # ── Tickets (4 fields) ─────────────────────────────────────────────────
    pre_loaded_tickets: Annotated[dict[str, TicketData], merge_dict]
    """Cached tickets keyed by ticket key (e.g., "SCRUM-244"). Pre-fetched + grown opportunistically."""

    tickets_referenced: Annotated[list[str], append_list]
    """Ticket keys mentioned in this meeting (in order of mention, duplicates OK)."""

    tickets_cache_ages: Annotated[dict[str, float], merge_dict]
    """Last-fetched timestamp per ticket. Used by freshness detection."""

    tickets_deferred: Annotated[list[str], append_list]
    """Ticket keys flagged to discuss in a future meeting."""

    # ── Conversation (3 fields) ────────────────────────────────────────────
    conversation_turns: Annotated[list[Turn], append_list]
    """Running log of all turns this session. Bounded by state pruning (max ~50)."""

    sam_last_response: Annotated[str, replace_value]
    """Sam's most recent spoken text. Used for addressee context."""

    open_questions: Annotated[list[str], replace_value]
    """Questions raised but not yet answered. Sam may proactively revisit."""

    # ── Participants (2 fields) ────────────────────────────────────────────
    participants: Annotated[list[str], replace_value]
    """Currently active attendees (excluding Sam). Updated on join/leave events."""

    participant_contributions: Annotated[dict[str, int], merge_dict]
    """Turn counts per participant — rough engagement tracker."""

    # ── Meta (4 fields) ────────────────────────────────────────────────────
    conversation_id: Annotated[str, replace_value]
    """Hash of sorted participant names. Stable across sessions with same attendees."""

    meeting_started_at: Annotated[float, replace_value]
    """Unix timestamp when the meeting started."""

    planned_duration_minutes: Annotated[int, replace_value]
    """Expected duration from setup (used to gauge pacing)."""

    prior_meeting_summaries: Annotated[list[PriorMeetingSummary], replace_value]
    """Up to 3 most recent summaries for this conversation_id. Loaded at start."""

    # ── Research cache (2 fields) ──────────────────────────────────────────
    web_search_cache: Annotated[dict[str, WebSearchResult], merge_dict]
    """Cached web search results keyed by query hash. Saves SerpAPI calls."""

    research_topics: Annotated[list[str], append_list]
    """Topics where research was performed this session."""


