# LangGraph reducers — how to merge updates from multiple nodes
# ══════════════════════════════════════════════════════════════════════════

def append_list(existing: list, new: list) -> list:
    """Reducer: append new items to existing list. Used for turn log, commitments, etc."""
    if new is None:
        return existing or []
    if existing is None:
        return list(new)
    return existing + list(new)


def merge_dict(existing: dict, new: dict) -> dict:
    """Reducer: merge new dict into existing (new keys overwrite). Used for caches."""
    if new is None:
        return existing or {}
    if existing is None:
        return dict(new)
    merged = dict(existing)
    merged.update(new)
    return merged


def replace_value(existing, new):
    """Reducer: always use the newest value. For scalars like current_topic_index."""
    return new if new is not None else existing


