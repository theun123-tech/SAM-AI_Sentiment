    async def _nlu_node(self, state: MeetingState) -> dict[str, Any]:
        """REAL — runs LLM-powered NLU extraction on the user turn.

        Extracts intent, entities, commitments, freshness hints, scope signals,
        and topic signals. Stores the NLUResult on self._pending_nlu for
        downstream nodes (policy_node, response_node) to read.

        Also records the turn in conversation_turns log.
        """
        pending = self._pending_turn
        if not pending:
            return {}

        speaker = pending["speaker"]
        user_text = pending["text"]

        # Build context for NLU
        agenda = state.get("agenda", [])
        agenda_titles = [a.title for a in agenda]
        current = get_current_topic(state)
        current_topic_str = current.title if current else ""
        preloaded = list(state.get("pre_loaded_tickets", {}).keys())

        # Recent conversation as formatted strings (last 6 turns)
        recent_turns = state.get("conversation_turns", [])[-6:]
        history_lines = [
            f"{t.speaker}: {t.text}" for t in recent_turns
        ]

        # Run NLU (never raises — always returns something)
        nlu = await self._nlu_analyzer.analyze(
            user_text=user_text,
            speaker=speaker,
            participants=state.get("participants", []),
            agenda_titles=agenda_titles,
            scope_in=state.get("scope_in", []),
            scope_out=state.get("scope_out", []),
            current_topic=current_topic_str,
            preloaded_tickets=preloaded,
            sam_last_response=state.get("sam_last_response", ""),
            conversation_history=history_lines,
        )

        # Store for downstream nodes
        self._pending_nlu = nlu

        # Record the turn with NLU-extracted intent and entities
        entity_values = [
            e.get("value", "") for e in nlu.entities
            if e.get("confidence", 0) >= 0.7
        ]
        new_turn = Turn(
            turn_number=pending["turn_number"],
            speaker=speaker,
            text=user_text,
            timestamp=pending["timestamp"],
            is_sam=False,
            intent=nlu.intent,
            entities=entity_values,
        )

        # Track ticket references (regex + LLM catches in NLU)
        ticket_keys = nlu.get_ticket_keys()

        # Update participant contributions
        contributions = dict(state.get("participant_contributions", {}))
        contributions[speaker] = contributions.get(speaker, 0) + 1

        print(f"[DialogueManager] {self._tag} 🔵 nlu: "
              f"turn_{pending['turn_number']} [{speaker}] intent={nlu.intent} "
              f"tickets={ticket_keys}")

        # Return updates — append_list reducers expect only NEW items
        updates: dict[str, Any] = {
            "conversation_turns": [new_turn],
            "participant_contributions": contributions,
        }
        if ticket_keys:
            updates["tickets_referenced"] = ticket_keys

        return updates

    async def _policy_node(self, state: MeetingState) -> dict[str, Any]:
        """REAL — applies rule-based policy to NLU result.

        Decides what Sam should do: respond, stay silent, redirect,
        transition topic, ask clarification, or deliver recap.

        Stores decision on self._pending_decision for response_node.
        Also applies state updates based on decision (e.g., topic transitions,
        scope deviation tracking, commitment extraction).
        """
        nlu = self._pending_nlu
        if not nlu:
            print(f"[DialogueManager] {self._tag} ⚠️  policy: no NLU result")
            return {}

        # Run policy engine
        decision = self._policy_engine.decide(nlu, state)
        self._pending_decision = decision
        # Phase 4A hotfix 1: persist the committed decision past the
        # finally block in process_turn. Used by get_last_decision().
        self._last_committed_decision = decision

        # ── State updates driven by policy decision ────────────────────
        updates: dict[str, Any] = {}

        # 1. Commitment extraction — if NLU got one with high confidence,
        #    add it to commitments_open. Applies regardless of action.
        if nlu.commitment and nlu.commitment.get("confidence", 0) >= 0.7:
            c = nlu.commitment
            pending_turn = self._pending_turn or {}
            new_commitment = Commitment(
                id=f"commit_{pending_turn.get('turn_number', 0)}",
                owner=c.get("owner", ""),
                action=c.get("action", ""),
                deadline=c.get("deadline"),
                confidence=c.get("confidence", 0.0),
                status="open",
                source_utterance=pending_turn.get("text", "")[:200],
                related_ticket=self._extract_related_ticket(nlu),
            )
            # Merge into existing open commitments (replace_value reducer)
            existing = list(state.get("commitments_open", []))
            existing.append(new_commitment)
            updates["commitments_open"] = existing
            print(f"[DialogueManager] {self._tag} 📝 Commitment: "
                  f"{new_commitment.owner} → {new_commitment.action[:40]} "
                  f"(due: {new_commitment.deadline})")

        # 2. Topic transition — if policy decided TRANSITION_TOPIC,
        #    update current_topic_index and mark the current one resolved.
        if decision.action == PolicyAction.TRANSITION_TOPIC:
            agenda = state.get("agenda", [])
            current_idx = state.get("current_topic_index", -1)
            resolved = list(state.get("topics_resolved", []))
            if 0 <= current_idx < len(agenda):
                agenda_item = agenda[current_idx]
                if agenda_item.id not in resolved:
                    resolved.append(agenda_item.id)
                    updates["topics_resolved"] = resolved  # replace_value
            # Advance index
            if current_idx + 1 < len(agenda):
                updates["current_topic_index"] = current_idx + 1
            else:
                updates["current_topic_index"] = -1  # no more topics

        # 3. Scope deviation — if user raised out-of-scope topic, log it.
        if decision.action == PolicyAction.SCOPE_REDIRECT:
            pending_turn = self._pending_turn or {}
            deviation = ScopeDeviation(
                turn_number=pending_turn.get("turn_number", 0),
                topic_raised=decision.scope_topic_raised or "unspecified",
                sam_handled_how="redirected",
            )
            updates["scope_deviations"] = [deviation]  # append_list

        print(f"[DialogueManager] {self._tag} 🔵 policy: "
              f"→ {decision.action.value}")

        return updates

    def _extract_related_ticket(self, nlu: NLUResult) -> Optional[str]:
        """If NLU entities include a ticket key, return it (for commitment linking)."""
        keys = nlu.get_ticket_keys()
        return keys[0] if keys else None

    async def _response_node(self, state: MeetingState) -> dict[str, Any]:
        """Triggers Sam's response via agent_callback — IF policy said so.

        Only fires for actions that should produce speech:
            - RESPOND_DIRECT
            - RESPOND_WITH_RESEARCH
            - ASK_CLARIFICATION
            - TRANSITION_TOPIC (Sam announces the transition)
            - SCOPE_REDIRECT (Sam redirects)
            - DELIVER_RECAP (end of meeting recap)

        STAY_SILENT skips the callback entirely.
        """
        pending = self._pending_turn
        decision = self._pending_decision
        if not pending or not decision:
            return {}

        text = pending["text"]
        speaker = pending["speaker"]

        if decision.action == PolicyAction.STAY_SILENT:
            print(f"[DialogueManager] {self._tag} 🤐 response: stay_silent, "
                  f"skipping agent call")
            return {}

        # For now, all other actions route to Agent.handle via the callback.
        # Week 5+ will add special handling for ASK_CLARIFICATION (uses
        # decision.clarification_options to prompt Sam's response) and
        # DELIVER_RECAP (generates grounded recap from state).
        #
        # In week 3, we pass through to the existing Agent pipeline.
        print(f"[DialogueManager] {self._tag} 🔵 response: "
              f"→ Agent.handle for {speaker} (action={decision.action.value})")

        try:
            await self._agent_callback(text, speaker, state)
        except Exception as e:
            print(f"[DialogueManager] {self._tag} ⚠️  response_node: "
                  f"agent callback failed: {e}")

        return {}

    async def _state_update_node(self, state: MeetingState) -> dict[str, Any]:
        """Finalizes state for this turn + persists to checkpoint.

        Most state updates happen in nlu_node and policy_node. This node
        handles cross-cutting updates and persistence.

        Week 4+ will add:
            - Ticket cache freshness updates
            - Open question resolution
            - Research result caching
        """
        # Persist state async (don't block the graph). Track task so close() awaits it.
        if self._checkpoint_mgr:
            # Clean up completed persist tasks to prevent unbounded growth
            self._pending_persists = [t for t in self._pending_persists if not t.done()]
            task = asyncio.create_task(self._persist_async())
            self._pending_persists.append(task)

        return {}

    async def _persist_async(self) -> None:
        """Background persistence — runs after graph completes."""
        try:
            await self._checkpoint_mgr.save_state(
                self._conversation_id,
                self._session_id,
                self._state,
            )
        except Exception as e:
            print(f"[DialogueManager] {self._tag} ⚠️  Async persist failed: {e}")