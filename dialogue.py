def build_nlu_prompt(
    user_text: str,
    speaker: str,
    participants: list,
    agenda_titles: list,
    scope_in: list,
    scope_out: list,
    current_topic: str,
    preloaded_tickets: list,
    sam_last_response: str,
    conversation_history: list,
) -> str:
    """Fill in the NLU_PROMPT template with concrete context.

    Bounds context size aggressively:
    - conversation_history capped at last 6 turns
    - sam_last_response truncated to 200 chars
    - all lists are human-joined, not JSON-serialized
    """
    # Compact participant list (names only)
    participants_str = ", ".join(participants) if participants else "(none recorded)"

    # Agenda titles as a numbered list
    if agenda_titles:
        agenda_str = "; ".join(f"{i+1}. {t}" for i, t in enumerate(agenda_titles))
    else:
        agenda_str = "(no agenda set)"

    # Scope lists
    scope_in_str = ", ".join(scope_in) if scope_in else "(not specified)"
    scope_out_str = ", ".join(scope_out) if scope_out else "(nothing excluded)"

    # Current topic
    current_topic_str = current_topic if current_topic else "(no active topic)"

    # Pre-loaded ticket keys
    tickets_str = ", ".join(preloaded_tickets) if preloaded_tickets else "(none)"

    # Sam's last response (truncated)
    sam_str = sam_last_response.strip()
    if len(sam_str) > 200:
        sam_str = sam_str[:197] + "..."
    if not sam_str:
        sam_str = "(nothing yet)"

    # Recent conversation — last 6 turns max
    if conversation_history:
        recent = conversation_history[-6:]
        history_str = "\n".join(f"  {line}" for line in recent)
    else:
        history_str = "  (no prior turns)"

    rendered = NLU_PROMPT.format(
        agenda_list=agenda_str,
        scope_in=scope_in_str,
        scope_out=scope_out_str,
        current_topic=current_topic_str,
        preloaded_tickets=tickets_str,
        participants=participants_str,
        sam_last_response=sam_str,
        conversation_history=history_str,
        speaker=speaker,
        user_text=user_text.replace('"', "'"),  # avoid breaking the JSON template
    )

    # ── Debug dump (opt-out via NLU_DEBUG_DUMP=0) ─────────────────────────
    # Appends every rendered NLU prompt to nlu_debug.txt with a timestamped
    # header. Lets you inspect EXACTLY what the model is seeing when it
    # makes scope_signal / intent decisions. Especially useful for
    # diagnosing "why did NLU say out_of_scope here?" without re-running.
    # File grows append-only — rotate/delete as needed.
    import os as _os
    if _os.environ.get("NLU_DEBUG_DUMP", "1") != "0":
        try:
            import datetime as _dt
            ts_str = _dt.datetime.now().strftime("%Y-%m-%d %H:%M:%S")
            sep = "═" * 70
            header = (
                f"\n{sep}\n"
                f"NLU CALL @ {ts_str}\n"
                f"speaker         : {speaker}\n"
                f"user_text       : {user_text!r}\n"
                f"scope_out_input : {scope_out!r}\n"
                f"scope_in_input  : {scope_in!r}\n"
                f"agenda          : {agenda_titles!r}\n"
                f"current_topic   : {current_topic!r}\n"
                f"{sep}\n"
                f"--- RENDERED PROMPT ---\n"
            )
            footer = f"\n--- END PROMPT ({len(rendered)} chars) ---\n"
            with open("nlu_debug.txt", "a", encoding="utf-8") as f:
                f.write(header)
                f.write(rendered)
                f.write(footer)
        except Exception as _dbg_err:
            # Never let debug dumping break the NLU call
            print(f"[NLU] ⚠️  debug dump failed (non-fatal): {_dbg_err}")

    return rendered

