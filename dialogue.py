def get_current_topic(state: MeetingState) -> Optional[AgendaItem]:
    """Return the agenda item currently being discussed, or None."""
    agenda = state.get("agenda", [])
    idx = state.get("current_topic_index", -1)
    if idx < 0 or idx >= len(agenda):
        return None
    return agenda[idx]


def get_state_size_bytes(state: MeetingState) -> int:
    """Approximate size of state in bytes (for monitoring bloat)."""
    import json
    try:
        return len(json.dumps(state_to_json(state)))
    except Exception:
        return -1


def state_summary_for_logging(state: MeetingState) -> str:
    """Compact one-line summary of state — for log output."""
    parts = []
    agenda = state.get("agenda", [])
    idx = state.get("current_topic_index", -1)
    if agenda and 0 <= idx < len(agenda):
        parts.append(f"topic={agenda[idx].title[:30]}")
    parts.append(f"open_commits={len(state.get('commitments_open', []))}")
    parts.append(f"tickets_cached={len(state.get('pre_loaded_tickets', {}))}")
    parts.append(f"turns={len(state.get('conversation_turns', []))}")
    parts.append(f"deviations={len(state.get('scope_deviations', []))}")
    return " | ".join(parts)

# ── Phase 2+3: cache-freshness helpers ────────────────────────────────────────

def is_ticket_fresh(
    state: MeetingState,
    ticket_key: str,
    max_age_seconds: float = 1800.0,
) -> bool:
    """Check if a cached ticket is still considered fresh.

    Default max_age_seconds=1800 (30 min) is session-scoped per the Phase 2
    decision: during a typical call, ticket state rarely changes, so cache
    stays valid for the whole meeting. Freshness hints from NLU override this.

    Returns False if ticket not in cache OR age exceeds threshold.
    """
    tickets = state.get("pre_loaded_tickets", {}) or {}
    if ticket_key not in tickets:
        return False
    ages = state.get("tickets_cache_ages", {}) or {}
    cached_at = ages.get(ticket_key)
    if cached_at is None:
        # No age recorded — treat as fresh (just loaded)
        return True
    return (time.time() - cached_at) <= max_age_seconds


