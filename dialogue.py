def state_to_json(state: MeetingState) -> dict:
    """Convert MeetingState to a fully JSON-safe dict.

    Handles dataclass sub-objects by calling their .to_dict() methods.
    Used by CheckpointManager for SQLite persistence.
    """
    def _serialize_list(lst, item_to_dict=None):
        if not lst:
            return []
        if item_to_dict:
            return [item_to_dict(item) if not isinstance(item, dict) else item for item in lst]
        return list(lst)

    def _serialize_dict(d, val_to_dict=None):
        if not d:
            return {}
        if val_to_dict:
            return {k: (val_to_dict(v) if not isinstance(v, dict) else v) for k, v in d.items()}
        return dict(d)

    return {
        # Agenda
        "agenda": _serialize_list(state.get("agenda", []), lambda x: x.to_dict()),
        "current_topic_index": state.get("current_topic_index", -1),
        "topics_resolved": list(state.get("topics_resolved", [])),
        "topics_deferred": list(state.get("topics_deferred", [])),
        # Scope
        "scope_in": list(state.get("scope_in", [])),
        "scope_out": list(state.get("scope_out", [])),
        "scope_deviations": _serialize_list(state.get("scope_deviations", []), lambda x: x.to_dict()),
        # Client profile (from UI "Know About Them")
        "client_profile": state.get("client_profile", "") or "",
        # Commitments
        "commitments_open": _serialize_list(state.get("commitments_open", []), lambda x: x.to_dict()),
        "commitments_resolved": _serialize_list(state.get("commitments_resolved", []), lambda x: x.to_dict()),
        "commitments_inherited": _serialize_list(state.get("commitments_inherited", []), lambda x: x.to_dict()),
        # Phase 6 step 2: recap flag
        "recap_delivered": bool(state.get("recap_delivered", False)),
        # Tickets
        "pre_loaded_tickets": _serialize_dict(state.get("pre_loaded_tickets", {}), lambda x: x.to_dict()),
        "tickets_referenced": list(state.get("tickets_referenced", [])),
        "tickets_cache_ages": dict(state.get("tickets_cache_ages", {})),
        "tickets_deferred": list(state.get("tickets_deferred", [])),
        # Conversation
        "conversation_turns": _serialize_list(state.get("conversation_turns", []), lambda x: x.to_dict()),
        "sam_last_response": state.get("sam_last_response", ""),
        "open_questions": list(state.get("open_questions", [])),
        # Participants
        "participants": list(state.get("participants", [])),
        "participant_contributions": dict(state.get("participant_contributions", {})),
        # Meta
        "conversation_id": state.get("conversation_id", ""),
        "meeting_started_at": state.get("meeting_started_at", 0.0),
        "planned_duration_minutes": state.get("planned_duration_minutes", 30),
        "prior_meeting_summaries": _serialize_list(
            state.get("prior_meeting_summaries", []), lambda x: x.to_dict()
        ),
        # Research
        "web_search_cache": _serialize_dict(
            state.get("web_search_cache", {}), lambda x: x.to_dict()
        ),
        "research_topics": list(state.get("research_topics", [])),
    }


