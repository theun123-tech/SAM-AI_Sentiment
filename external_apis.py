    async def search_tickets(self, query: str, max_results: int = 10) -> list[dict]:
        """Lightweight autocomplete search — key + summary + status only.

        Used by the setup UI ticket picker. Matches on summary text and key.
        Returns at most max_results, sorted by recency. Never raises —
        returns empty list on any error (UI should gracefully show "no results").
        """
        if not query or not query.strip():
            return []
        q = query.strip().replace('"', '\\"')
        # Try key-exact match first if it looks like a key (e.g., "SCRUM-244" or "244")
        import re as _re
        key_match = _re.match(r'^([A-Z]+-)?(\d+)$', q, _re.IGNORECASE)
        if key_match:
            key_candidate = key_match.group(0).upper()
            if "-" not in key_candidate:
                key_candidate = f"{self.project}-{key_candidate}"
            try:
                ticket = await self.get_ticket(key_candidate)
                if ticket:
                    return [ticket]
            except Exception:
                pass  # Fall through to text search
        # Text search across summary and description
        jql = f'project = {self.project} AND (summary ~ "{q}" OR text ~ "{q}") ORDER BY updated DESC'
        try:
            return await self.search_jql(jql, max_results=max_results)
        except Exception as e:
            print(f"[Jira] ⚠️  search_tickets failed: {e}")
            return []

    async def get_tickets_batch(self, keys: list[str]) -> list[dict]:
        """Bulk-fetch specific ticket keys (used for pre-fetching setup tickets).

        Returns list of tickets in the order provided (skipping any that fail).
        Uses JQL `key in (...)` which is 1 API call regardless of list size,
        MUCH cheaper than N individual get_ticket() calls.
        """
        # Sanitize and dedupe while preserving order
        seen = set()
        clean_keys = []
        for k in keys:
            if not k:
                continue
            k = k.strip().upper()
            if "-" not in k:
                continue  # invalid format
            if k not in seen:
                seen.add(k)
                clean_keys.append(k)
        if not clean_keys:
            return []
        # JQL: `key in ("X", "Y", "Z")` — single round-trip
        key_list = ', '.join(f'"{k}"' for k in clean_keys)
        jql = f'key in ({key_list})'
        try:
            tickets = await self.search_jql(jql, max_results=min(len(clean_keys), 100))
        except Exception as e:
            print(f"[Jira] ⚠️  get_tickets_batch failed: {e}")
            return []
        # Reorder to match input order
        by_key = {t.get("key"): t for t in tickets}
        return [by_key[k] for k in clean_keys if k in by_key]

