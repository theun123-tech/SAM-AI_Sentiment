def save_conversation_summary(
    conversation_id: str, summary: dict, participants: list, session_id: str = ""
):
    """Save a meeting summary under a conversation_id.

    summary: dict with topics, decisions, commitments, open_items,
             tickets_referenced, summary_text (see SESSION_SUMMARY_PROMPT).
    participants: list of attendee names (for debugging, not used for lookup).
    session_id: optional link back to the raw session record.
    """
    if not conversation_id:
        print("[Store] ⚠️  save_conversation_summary: empty conversation_id, skipping")
        return

    with _lock:
        summaries = _load_json(CONVERSATIONS_FILE, [])

        entry = {
            "conversation_id": conversation_id,
            "participants": sorted(participants) if participants else [],
            "date": time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime()),
            "session_id": session_id,
            "summary": summary,
        }
        summaries.insert(0, entry)

        # Cap total stored summaries to prevent runaway growth (keep newest 1000)
        if len(summaries) > 1000:
            summaries = summaries[:1000]

        _save_json(CONVERSATIONS_FILE, summaries)
        print(
            f"[Store] 💾 Saved summary for conv_id={conversation_id[:8]}... "
            f"participants={entry['participants']}"
        )


