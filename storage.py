# ══════════════════════════════════════════════════════════════════════════════


def _find_or_create_insights_record(records: list, session_id: str) -> dict:
    """Find an existing record for session_id or create and append a stub."""
    for rec in records:
        if rec.get("session_id") == session_id:
            return rec
    stub = {
        "session_id": session_id,
        "session_type": "",
        "date": "",
        "participants": [],
        "transcript": [],
        "session_summary": {},
    }
    records.insert(0, stub)
    return stub


def append_utterance_insight(session_id: str, utterance_record: dict) -> None:
    """Append one per-utterance insight record to INSIGHTS_FILE in real-time.

    Thread-safe. Retries once after 200ms on OSError (file locked by
    another process). Never raises — logging must not break main flow.

    utterance_record shape:
        {
            "timestamp": "2026-05-21T16:40:54Z",
            "speaker": "Dev Sam",
            "utterance": "I've been working on ...",
            "sentiment": {"label": "positive", "score": 0.87},  # score from Deepgram (null if unavailable)
            "emotions":  {"dominant": "focused", "scores": {...}},
            "intent":    {"label": "providing_update", "score": 0.5},
            "filler_triggered": false,
            "pause_detected": false,
            "adaptive_endpointing_used": "fast"|"medium"|"slow"|null,
            "active_listening_token_played": "Hmm"|null,
            "bridge_filler_played": null,
        }
    """
    for attempt in range(2):
        try:
            with _lock:
                records = _load_json(INSIGHTS_FILE, [])
                rec = _find_or_create_insights_record(records, session_id)
                rec["transcript"].append(utterance_record)
                _save_json(INSIGHTS_FILE, records)
            return
        except OSError as e:
            if attempt == 0:
                import time as _t

                _t.sleep(0.2)
            else:
                print(f"[Insights] ⚠️  append_utterance_insight failed after retry: {e}")
        except Exception as e:
            print(f"[Insights] ⚠️  append_utterance_insight error (non-fatal): {e}")
            return


def save_session_insights(
    session_id: str,
    session_type: str,
    date: str,
    participants: list,
    summary: dict,
) -> None:
    """Merge session-level metadata and summary into the insights record.

    Called once when a session ends (after all per-utterance appends).
    Fills in session_type, date, participants, and session_summary fields.
    Thread-safe. Never raises.
    """
    try:
        with _lock:
            records = _load_json(INSIGHTS_FILE, [])
            rec = _find_or_create_insights_record(records, session_id)
            rec["session_type"] = session_type
            rec["date"] = date
            rec["participants"] = participants
            rec["session_summary"] = summary
            _save_json(INSIGHTS_FILE, records)
        sid = session_id[:12]
        print(
            f"[Insights] 💾 Saved session summary for {sid} "
            f"({session_type}, {len(rec['transcript'])} utterances)"
        )
    except Exception as e:
        print(f"[Insights] ⚠️  save_session_insights failed (non-fatal): {e}")


def get_session_insights(session_id: str) -> dict | None:
    """Load the full insights record for a session. Returns None if not found."""
    records = _load_json(INSIGHTS_FILE, [])
    for rec in records:
        if rec.get("session_id") == session_id:
            return rec
    return None
