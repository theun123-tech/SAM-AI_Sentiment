def save_meeting_setup(session_id: str, setup: dict):
    """Store setup data for a session (before Sam joins).

    setup shape:
        {
            "agenda": [{"id": "topic_1", "title": "...", "status": "pending"}, ...],
            "scope_in": [str, ...],
            "scope_out": [str, ...],
            "ticket_keys": ["SCRUM-244", ...],
            "planned_duration_minutes": 30,
            "template_name": str or None  (if loaded from template)
        }
    """
    with _lock:
        setups = _load_json(MEETING_SETUPS_FILE, {})
        setup["saved_at"] = time.time()
        setups[session_id] = setup
        # Prune: keep last 50 by timestamp
        if len(setups) > 50:
            items = sorted(
                setups.items(), key=lambda kv: kv[1].get("saved_at", 0), reverse=True
            )[:50]
            setups = dict(items)
        _save_json(MEETING_SETUPS_FILE, setups)


def get_meeting_setup(session_id: str) -> dict | None:
    """Load setup for a session. Returns None if not found."""
    with _lock:
        setups = _load_json(MEETING_SETUPS_FILE, {})
    return setups.get(session_id)


def delete_meeting_setup(session_id: str):
    """Remove setup (e.g., after session ends). Non-fatal on miss."""
    with _lock:
        setups = _load_json(MEETING_SETUPS_FILE, {})
        setups.pop(session_id, None)
        _save_json(MEETING_SETUPS_FILE, setups)


