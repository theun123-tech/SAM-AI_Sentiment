def save_agenda_template(user: str, name: str, template: dict):
    """Save or update an agenda template for a user.

    template shape: same as setup, minus session-specific fields
        {
            "agenda": [...],
            "scope_in": [...],
            "scope_out": [...],
            "ticket_keys": [...],  (optional — commonly-used tickets)
        }
    """
    if not user or not name:
        raise ValueError("user and name required")
    with _lock:
        all_templates = _load_json(AGENDA_TEMPLATES_FILE, {})
        user_templates = all_templates.get(user, {})
        template["name"] = name
        template["updated_at"] = time.time()
        user_templates[name] = template
        all_templates[user] = user_templates
        _save_json(AGENDA_TEMPLATES_FILE, all_templates)


def get_agenda_templates(user: str) -> list[dict]:
    """Return list of templates for a user, most-recently-updated first."""
    with _lock:
        all_templates = _load_json(AGENDA_TEMPLATES_FILE, {})
    user_templates = all_templates.get(user, {})
    items = list(user_templates.values())
    items.sort(key=lambda t: t.get("updated_at", 0), reverse=True)
    return items


def delete_agenda_template(user: str, name: str) -> bool:
    """Remove a template. Returns True if removed, False if not found."""
    with _lock:
        all_templates = _load_json(AGENDA_TEMPLATES_FILE, {})
        user_templates = all_templates.get(user, {})
        if name not in user_templates:
            return False
        user_templates.pop(name)
        all_templates[user] = user_templates
        _save_json(AGENDA_TEMPLATES_FILE, all_templates)
    return True


